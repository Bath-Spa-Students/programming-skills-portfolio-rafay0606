## Exercise 1

# Creating a dictionary to store information of a friend
person_info = {
    'first_name': 'Ahmad',
    'last_name': 'Abdullah',
    'age': 19,
    'city': 'Mamourah'
}

# Printing all the info stored in the dictionary
print('First Name: ' + person_info['first_name'])
print('Last Name: ' + person_info['last_name'])
print('Age: ' + str(person_info['age']))  # Converting the age to a string for printing
print('City: ' + person_info['city'])
